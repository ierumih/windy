package com.windy.vo;

public class Bike {
	int bike_num;
	String bike_brand, bike_price, bike_pic, bike_name, bike_content, bike_logo;
	
	public String getBike_logo() {
		return bike_logo;
	}
	public void setBike_logo(String bike_logo) {
		this.bike_logo = bike_logo;
	}
	public int getBike_num() {
		return bike_num;
	}
	public void setBike_num(int bike_num) {
		this.bike_num = bike_num;
	}
	public String getBike_brand() {
		return bike_brand;
	}
	public void setBike_brand(String bike_brand) {
		this.bike_brand = bike_brand;
	}
	public String getBike_price() {
		return bike_price;
	}
	public void setBike_price(String bike_price) {
		this.bike_price = bike_price;
	}
	public String getBike_pic() {
		return bike_pic;
	}
	public void setBike_pic(String bike_pic) {
		this.bike_pic = bike_pic;
	}
	public String getBike_name() {
		return bike_name;
	}
	public void setBike_name(String bike_name) {
		this.bike_name = bike_name;
	}
	public String getBike_content() {
		return bike_content;
	}
	public void setBike_content(String bike_content) {
		this.bike_content = bike_content;
	}
	
}
