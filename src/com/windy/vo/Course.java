public class Course {
	String c_name, c_area, c_location, c_cont, c_img, nick;
	int c_like, c_num, c_date, c_view;

	public int getC_num() {
		return c_num;
	}

	public void setC_num(int c_num) {
		this.c_num = c_num;
	}

	public int getC_date() {
		return c_date;
	}

	public void setC_date(int c_date) {
		this.c_date = c_date;
	}

	public int getC_view() {
		return c_view;
	}

	public void setC_view(int c_view) {
		this.c_view = c_view;
	}

	public int getC_like() {
		return c_like;
	}

	public void setC_like(int c_like) {
		this.c_like = c_like;
	}

	public String getC_name() {
		return c_name;
	}

	public void setC_name(String c_name) {
		this.c_name = c_name;
	}

	public String getC_area() {
		return c_area;
	}

	public void setC_area(String c_area) {
		this.c_area = c_area;
	}

	public String getC_location() {
		return c_location;
	}

	public void setC_location(String c_location) {
		this.c_location = c_location;
	}

	public String getC_cont() {
		return c_cont;
	}

	public void setC_cont(String c_cont) {
		this.c_cont = c_cont;
	}

	public String getC_img() {
		return c_img;
	}

	public void setC_img(String c_img) {
		this.c_img = c_img;
	}

	public String getNick() {
		return nick;
	}

	public void setNick(String nick) {
		this.nick = nick;
	}
}
